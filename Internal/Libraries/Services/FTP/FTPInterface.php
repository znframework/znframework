	//----------------------------------------------------------------------------------------------------
	// permission()
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $path: empty
	// @param int $type   : 0755
	//
	//----------------------------------------------------------------------------------------------------
	public function permission($path, $type);
	
	//----------------------------------------------------------------------------------------------------
	// files()
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $path     : empty
	// @param string $extension: empty
	//
	//----------------------------------------------------------------------------------------------------
	public function files($path, $extension);
	
	//----------------------------------------------------------------------------------------------------
	// fileSize()
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $path   : empty
	// @param string $type   : b, kb, mb, gb
	// @param int    $decimal: 2
	//
	//----------------------------------------------------------------------------------------------------
	public function fileSize($path, $type, $decimal);
}