    //--------------------------------------------------------------------------------------------------------
    protected function _to($to, $name, $type = 'to')
    {
        if( is_array($to) )
        {
            if( ! empty($to) ) foreach( $to as $key => $val )
            {
                if( isEmail($key) )
                {
                    $this->{$type}[$key] = $val;
                }
            }
        }
        else
        {
            if( isEmail($to) )
            {
                $this->{$type}[$to] = $name;
            }
            else
            {
                throw new InvalidArgumentException('Error', 'emailParameter', '1.('.$type.')');
            }
        }
    }

