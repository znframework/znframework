    //--------------------------------------------------------------------------------------------------------
    public function rotateZ(String $argument) : Transform
    {
        $this->_numeric(__FUNCTION__, $argument, 'deg');

        return $this;
    }

    //--------------------------------------------------------------------------------------------------------
    // Scale
    //--------------------------------------------------------------------------------------------------------
    //
    // @param int $x
    // @param int $y
    //
    //--------------------------------------------------------------------------------------------------------
    public function scale(Int $x, Int $y) : Transform
    {
        $this->_object(__FUNCTION__, "$x,$y");

        return $this;
    }

    //--------------------------------------------------------------------------------------------------------
    // Scale 3d
    //--------------------------------------------------------------------------------------------------------
    //
    // @param int $x
    // @param int $y
