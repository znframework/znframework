    //--------------------------------------------------------------------------------------------------------
    // Matrix
    //--------------------------------------------------------------------------------------------------------
    //
    // @param numeric variadic $arguments
    //
    //--------------------------------------------------------------------------------------------------------
    public function matrix(...$arguments) : Transform
    {
        $this->_object(__FUNCTION__, $arguments);

        return $this;
    }

    //--------------------------------------------------------------------------------------------------------
    // Matrix 3d
    //--------------------------------------------------------------------------------------------------------
    //
    // @param numeric variadic $arguments
    //
    //--------------------------------------------------------------------------------------------------------
    public function matrix3d(...$arguments) : Transform
