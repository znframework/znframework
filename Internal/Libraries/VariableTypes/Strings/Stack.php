	// Pad
	//----------------------------------------------------------------------------------------------------
	// 
	// @param numeric $count
	// @param string  $chars
	// @param string  $type
	//
	//----------------------------------------------------------------------------------------------------
	public function pad($count = 1, $chars = ' ', $type = 'right')
	{
		$this->data = \Strings::pad($this->data, $count, $chars, $type);
			
		return $this;
	}
	
