		return $this;
	}	

	//----------------------------------------------------------------------------------------------------
	// Lower Case
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $encoding
	//
	//----------------------------------------------------------------------------------------------------
	public function lowerCase($encoding = 'utf-8')
