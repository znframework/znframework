	// Divide
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string  $seperator
	// @param numeric $index
	//
	//----------------------------------------------------------------------------------------------------
	public function divide($separator = "|", $index = 0)
	{
		$this->data = \Strings::divide($this->data, $separator, $index);
			
		return $this;
	}
	
	//----------------------------------------------------------------------------------------------------
	// Get
	//----------------------------------------------------------------------------------------------------
	// 
	// @param  void
	// @return string
	//
	//----------------------------------------------------------------------------------------------------
	public function get()
	{
		$data = $this->data;
		
		$this->data = '';
		
		return $data;
	}
