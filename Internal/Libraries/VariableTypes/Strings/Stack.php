	// Repeat
	//----------------------------------------------------------------------------------------------------
	// 
	// @param numeric $count
	//
	//----------------------------------------------------------------------------------------------------
	public function repeat($count = 1)
	{
		$this->data = \Strings::repeat($this->data, $count);
		
		return $this;
	}
	
