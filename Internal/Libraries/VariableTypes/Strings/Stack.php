	// Apportion
	//----------------------------------------------------------------------------------------------------
	// 
	// @param numeric $length
	// @param string  $end
	//
	//----------------------------------------------------------------------------------------------------
	public function apportion($length = 76, $end = "\r\n")
	{
		$this->data = \Strings::apportion($this->data, $length, $end);
			
		return $this;
	}
	
