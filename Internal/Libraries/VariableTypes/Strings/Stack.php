	// 
	// @param string $delimiter
	// @param array  $array
	//
	//----------------------------------------------------------------------------------------------------
	public function replace($oldChar = '', $newChar = '', $case = true)
	{
		$this->data = \Strings::replace($this->data, $oldChar, $newChar, $case);
		
		return $this;
	}
	
