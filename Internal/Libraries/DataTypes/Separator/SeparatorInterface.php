	public function encode(Array $data, String $key = NULL, String $separator = NULL) : String;
	
	//----------------------------------------------------------------------------------------------------
	// Decode
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $word
	// @param string $key
	// @param string $separator
