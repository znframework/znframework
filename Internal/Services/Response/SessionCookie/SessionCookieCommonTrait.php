<?php namespace ZN\Services\Response;

use Classes, Strings, Arrays;

trait SessionCookieCommonTrait
{
    //--------------------------------------------------------------------------------------------------------
    //
    // Author     : Ozan UYKUN <ozanbote@gmail.com>
    // Site       : www.znframework.com
    // License    : The MIT License
    // Copyright  : (c) 2012-2016, znframework.com
    //
    //--------------------------------------------------------------------------------------------------------

    //--------------------------------------------------------------------------------------------------------
    // Regenerate
    //--------------------------------------------------------------------------------------------------------
    //
    // @var bool
    //
    //--------------------------------------------------------------------------------------------------------
    protected $regenerate = true;

    //--------------------------------------------------------------------------------------------------------
    // Encode
    //--------------------------------------------------------------------------------------------------------
    //
    // @var array
    //
    //--------------------------------------------------------------------------------------------------------
    protected $encode = [];

    //--------------------------------------------------------------------------------------------------------
    // Magic Call
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $method
    // @param array  $parameters
    //
    //--------------------------------------------------------------------------------------------------------
    public function __call($method, $parameters)
    {
        $class = Classes::onlyName(__CLASS__);
        $split = Strings::splitUpperCase($method);

        if( Arrays::getLast($split) === 'Delete' )
        {
            $method = 'delete';

            return $class::delete($split[0]);
        }

        if( $method === 'all' )
        {
            $method = 'selectAll';

            return $class::$method();
        }

        if( $param = ($parameters[0] ?? NULL) )
        {
            return $class::insert($method, $param);
        }

        return $class::select($method);
    }

    //--------------------------------------------------------------------------------------------------------
    // Encode
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $name
    // @param string $value
    //
    //--------------------------------------------------------------------------------------------------------
    public function encode(String $nameAlgo = NULL, String $valueAlgo = NULL)
    {
        $this->encode['name']  = $nameAlgo;
        $this->encode['value'] = $valueAlgo;

        return $this;
    }

    //--------------------------------------------------------------------------------------------------------
    // Decode
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $nameAlgo
    //
    //--------------------------------------------------------------------------------------------------------
    public function decode(String $nameAlgo)
    {
        $this->encode['name'] = $nameAlgo;

        return $this;
    }

    //--------------------------------------------------------------------------------------------------------
    // Regenerate
    //--------------------------------------------------------------------------------------------------------
    //
    // @param bool $regenerate
    //
    //--------------------------------------------------------------------------------------------------------
    public function regenerate(Bool $regenerate = true)
    {
        $this->regenerate = $regenerate;

        return $this;
    }

    //--------------------------------------------------------------------------------------------------------
    // Protected Methods
    //--------------------------------------------------------------------------------------------------------
    //
    // defaultVariable()
    //
    //--------------------------------------------------------------------------------------------------------
    protected function defaultVariable()
    {
        $this->name       = NULL;
        $this->value      = NULL;
        $this->encode     = [];
        $this->regenerate = true;
    }
}
