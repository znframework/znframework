use ZN\Database\DriverUser;

class PostgresUser extends DriverUser
{
    //--------------------------------------------------------------------------------------------------------
    // Protected Postgre Quote Options
    //--------------------------------------------------------------------------------------------------------
    //
    // @var array
    //
    //--------------------------------------------------------------------------------------------------------
    protected $postgreQuoteOptions =
    [
        'PASSWORD',
        'VALID UNTIL'
    ];

    //--------------------------------------------------------------------------------------------------------
    // name()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $name: USER()
    //
    //--------------------------------------------------------------------------------------------------------
    public function name($name)
    {
        $this->name = $name;
    }

    //--------------------------------------------------------------------------------------------------------
    // option()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $name
    // @param string $value
    //
    //--------------------------------------------------------------------------------------------------------
    public function option($option, $value)
    {
        if( ! empty($this->postgreQuoteOptions[\Autoloader::upper($option)]) )
        {
            $value = presuffix($value, '\'');
        }

        $this->parameters['option'] = $option.' '.$value;
    }

    //--------------------------------------------------------------------------------------------------------
    // create()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $user
    // @param array  $parameters
    //
    //--------------------------------------------------------------------------------------------------------
    public function create($user)
    {
        $query = 'CREATE USER '.
                 $user.
                 ( ! empty($this->parameters['option']) ? ' '.$this->parameters['option'] : '' );

        $this->_resetQuery();

        return $query;
    }

    //--------------------------------------------------------------------------------------------------------
    // drop()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $user
    //
    //--------------------------------------------------------------------------------------------------------
    public function drop($user)
    {
        $query = 'DROP USER '.$user;

        $this->_resetQuery();

        return $query;
    }

    //--------------------------------------------------------------------------------------------------------
    // alter()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $user
    // @param array  $parameters
    //
    //--------------------------------------------------------------------------------------------------------
    public function alter($user)
    {
        $query = 'ALTER USER '.
                 $user.
                 ( ! empty($this->parameters['option']) ? ' '.$this->parameters['option'] : '' );

        $this->_resetQuery();

        return $query;
    }
}
