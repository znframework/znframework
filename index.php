$system['benchmarkingTest']      = false;

//----------------------------------------------------------------------------------------------------
// Application Directory                                                                  
//----------------------------------------------------------------------------------------------------
//
// Uygulamanın çalışcağı dizini belirtilir.				     			 	  		  
//
//----------------------------------------------------------------------------------------------------
$system['applicationDirectory']  = 'Application';

//----------------------------------------------------------------------------------------------------
// Application Type                                                                  
//----------------------------------------------------------------------------------------------------
//
